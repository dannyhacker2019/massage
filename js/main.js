const form = document.getElementById('bookingForm');
const statusDiv = document.getElementById('status');

form.addEventListener('submit', async (e) => {
  e.preventDefault();

  const data = {
    name: form.name.value,
    phone: form.phone.value,
    service: form.service.value,
    date: form.date.value,
    //message: form.message.value
  };

  try {
    const res = await fetch('http://127.0.0.1:5000/book', { // Update to live URL later
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(data)
    });

    if (res.ok) {
      statusDiv.textContent = 'Booking sent successfully! ðŸ’Œ';
      statusDiv.style.color = 'green';
      form.reset();
    } else {
      statusDiv.textContent = 'Error sending booking. Try again.';
      statusDiv.style.color = 'red';
    }
  } catch (err) {
    statusDiv.textContent = 'Cannot reach backend. Make sure it is running.';
    statusDiv.style.color = 'red';
  }
});